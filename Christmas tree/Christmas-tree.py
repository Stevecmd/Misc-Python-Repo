number=int(input("How many rows do you want on your tree? "))

def Tree(number):
    for x in range(number):
        noOfStars = x
        stars = "*" * noOfStars
        blankSpace = " " * (number - x)
        stemMiddle = blankSpace * (number-1)
        
        #logic
        print(blankSpace + stars + "*" + stars)
    print(stemMiddle + "||")        

